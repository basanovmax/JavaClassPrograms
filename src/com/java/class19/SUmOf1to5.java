package com.java.class19;

public class SUmOf1to5 {
    public static void main(String[] args) {

        //1+2+3+4+5

        int sum = 0;
        for (int i=1; i<=5;i++) {
            sum+=i;
        }
        System.out.println(sum);

        int sum1 = 1;
        for (int i=1; i<=5;i++) {
            sum1 *=i;
        }
        System.out.println(sum1);

      //  Java program to find sum of 10 + 1 + 9 + 2 + 8 + 3 + 7 + 4 + 6 + 5

    }

}
